const mongoose = require('mongoose')

const otherIssuedLoanSchema = new mongoose.Schema({
    company: {type: String, ref: 'Company', required: true},
    loan: {type: String, ref: "Issued Loan", required: true},
    otherNumber: {type: String},
    otherName: {type: String},
    amount: {type: Number},
    percentage: {type: Number},
    rate: {type: Number},
    quantity: Number,
    date: {type: Date},
    grossWt: {type: Number},
    netWt: {type: Number},
    month: {type: String},
    renewalDate: {type: Date},
    closeDate: {type: Date},
    otherCharge: {type: Number},
    closingAmount: {type: Number},
    interestAmount: {type: Number},
    amountPaid: {type: Number, default: 0},
    paymentMode: String,
    cashAmount: Number,
    pendingCashAmount: {type: Number, default: 0},
    payingCashAmount: {type: Number, default: 0},
    payingBankAmount: {type: Number, default: 0},
    pendingBankAmount: {type: Number, default: 0},
    bankAmount: Number,
    bankDetails: Object,
    remarks: {type: String},
    isActive: {type: Boolean, default: true},
    deleted_at: {type: Date, default: null},
})

module.exports = mongoose.model('Other Issued Loan', otherIssuedLoanSchema);